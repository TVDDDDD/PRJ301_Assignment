package servlet;

import dao.LeaveDAO;
import model.User;
import jakarta.servlet.ServletException;
import jakarta.servlet.annotation.WebServlet;
import jakarta.servlet.http.HttpServlet;
import jakarta.servlet.http.HttpServletRequest;
import jakarta.servlet.http.HttpServletResponse;
import jakarta.servlet.http.HttpSession;
import java.io.IOException;

// Servlet for listing leave requests
@WebServlet("/list_leaves")
public class ListLeaveServlet extends HttpServlet {
    @Override
    protected void doGet(HttpServletRequest request, HttpServletResponse response)
            throws ServletException, IOException {
        HttpSession session = request.getSession();
        User user = (User) session.getAttribute("user");
        if (user == null) {
            response.sendRedirect("login.jsp");
            return;
        }

        LeaveDAO dao = new LeaveDAO();
        request.setAttribute("leaves", dao.getLeavesByUser(user.getUsername()));
        request.getRequestDispatcher("list_leaves.jsp").forward(request, response);
    }
}